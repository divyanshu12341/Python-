#Binary representation of a number
#prefix --> 0b
print(bin(18))

#Decimal representation of a number 
print(int("10010",2))

#AND OPERATOR BITWISE 
x = 3
y = 5 
print(x & y)

#OR OPERATOR BITWISE 
x = 3
y = 5
print(x | y)

#XOR OPERATOR BITWISE ^ 
x = 3
y = 5
print(x ^ y)

#LEFT SHIFT OPERATOR BITWISE 
#IT MULTIPLES RESULT WITH 2 
x = 3 
print(x << 1)
print(x << 2)
print(x << 3)

#RIGHT SHIFT OPERATOR BITWISE 
#IT DIVIDES RESULT BY 2 
x = 21
print(x>>1)
print(x>>2)
print(x>>3)

#BITWISE NOT OPERATOR 
x = 21 
print(~x)